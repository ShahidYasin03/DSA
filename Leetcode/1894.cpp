class Solution
{
public:
    int chalkReplacer(vector<int> &chalk, int k)
    {
        long sum = 0;
        int n = chalk.size();
        for(int i = 0; i < n; i++)
        {
            sum += chalk[i];
            if(sum > k)
            {
                break;
            }
        }
        while(sum < k)
        {
            k -= sum;
        }
        if(k == 0)
        {
            return 0;
        }
        for(int i = 0; i< n; i++)
        {
            if(chalk[i] > k)
            {
                return i;
            }else{
                k -= chalk[i];
            }
        }
        return 0;
    }
};

